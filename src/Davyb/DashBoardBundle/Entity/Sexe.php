<?php

namespace Davyb\DashBoardBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Sexe
 */
class Sexe
{
    /**
     * @var string
     */
    private $type;

    /**
     * @var integer
     */
    private $idSexe;


    /**
     * Set type
     *
     * @param string $type
     * @return Sexe
     */
    public function setType($type)
    {
        $this->type = $type;

        return $this;
    }

    /**
     * Get type
     *
     * @return string 
     */
    public function getType()
    {
        return $this->type;
    }

    /**
     * Get idSexe
     *
     * @return integer 
     */
    public function getIdSexe()
    {
        return $this->idSexe;
    }
}
